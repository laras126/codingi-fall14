	<p>
		&quot;Mockingjay&quot; is without question the most brutal of the trilogy. Nobody emerges unscathed &#8212; very bad things happen to everyone from fan favorites down to characters so minor a reader has to pause and think, &quot;Now, who was that again?&quot; before recoiling in horror at their fate. Collins doesn't take war lightly &#8212; her characters debate the morality involved in tactics used to try to overthrow the rotting, immoral government, and they pay a high cost for those tactics. It is also an entirely gripping read. In Katniss, Collins has crafted a heroine so fierce and tenacious that this reader will follow her anywhere.&quot;
