	<p>
		&quot;Throughout the trilogy, Collins has asked readers to consider heavy questions. What level of violence is justified to achieve needed change? How much integrity can one compromise for a just end? To what extent does responsibility to others demand sacrifice of self? How much control does anyone have over the construction of self? Katniss is the ideal vehicle for this dialogue, her present-tense narration constantly putting her own motivations and even identity under scrutiny.It's not giving away anything to reveal that Katniss will be tested sorely, that allegiances will shift, that heart-thumping scenes of combat will yield to anguished reflection, that she and readers will find themselves always wondering just whom to trust, that she and readers will lose friends they love. In the final analysis, this is exactly the book its fans have been hoping for. It will grab them and not let go, and if it leaves them with questions, well, then, it's probably exactly the book Collins was hoping for, too.&quot;
