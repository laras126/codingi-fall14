		                <dt>Why was Crispin not in <em>Back to the Future Part II</em> or <em>Back to the Future Part III</em>?</dt>
		                <div class="answer">
		                    <dd>
		                    	The producer Bob Gale made false statements about Crispin on those films' Blu-Ray DVDs. Bob Gale claimed that Crispin demanded the same salary as Michael J. Fox. This is not true. Crispin was not given the entire screenplay for <em>Back to the Future</em> until after he was cast in the film. When they were filming the end sequence, Crispin mentioned that he felt the characters were being rewarded with monetary elements at the end of the screenplay, which led to a moral for the film that “money equals happiness.” The producers did not care for this statement. <br /><br />Afterward, the producers offered Crispin less than half the salary of what Tom Wilson and Lea Thomson were making for the sequels. This did not seem fair to Crispin or his agents, so no agreement was reached for Crispin to appear in the sequels. The producers then utilized plaster casts of Crispin's face they had from the first film to make up another actor to look like Crispin utilizing prosthetics made from Crispin's facial features. In <em>Back to the Future Part II</em>, the producers inter-spliced the other actor's performance with a very small amount of footage of Crispin from the original film in order to fool audiences in to believing Crispin appeared in the sequels. Because of Crispin's lawsuit there became rules in the Screen Actor's Guild that makes it so producers can never do this sort of activity to other actors again.    
		                    </dd>
